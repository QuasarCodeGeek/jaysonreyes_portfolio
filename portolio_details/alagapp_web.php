              <h2>Abstract</h2>
              <p>
              In the modern world, keeping track of a pet's vaccination records and prescription history was a taxing endeavor. The AlagApp: Veterinary Clinic Pet Monitoring and Online Consultation System was created to meet this difficulty. By making it easier to manage pet profiles, owner data, vaccination and prescription records, appointment scheduling, and online consultations between clients and veterinarians, this system aimed to streamline the procedure. The system was developed using Rapid Application Development (RAD), and the front end made use of the HTML, CSS, JavaScript, and Bootstrap libraries, while the back end made use of PHP and MySQL. VS Code and XAMPP was used for development including Canva and Balsamiq, browsers such as Google Chrome, Brave, and Microsoft Edge. Data were gathered over a 7-day period with the system placed at the Pet Monarch Small Animal Clinic and Clinica Animalia as primary and secondary clients, respectively. The researcher used convenience sampling in selecting respondents of the study where thirty (30) VetMed students, two (2) veterinarians from the Pet Monarch Small Animal Clinic, one (1) vet from the Clinica Animalia Echague Branch, and three (3) technical experts from the CCISCT Department provided input. The evaluation in accordance with the requirements of ISO 25010 by a group of responders made up of veterinarians, pet owners, and technical experts. A Grand Mean score of 4.62, which indicated a high level of satisfaction across the categories of functionality, efficiency, usability, and portability, was determined by the evaluation. These results demonstrated that the system successfully achieved its goal of facilitating pet record monitoring and client-vet interaction.
